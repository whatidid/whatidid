						<h4><strong>Ödev 1 şöyledir:</strong></h4>
						<p>Derste Euler metoduyla yapılan DC motor model simülasyonunu Runga-Kutta metoduna göre düzenleyiniz. Giriş voltajını rampa şeklinde sıfırdan itibaren artırarak motoru kaldırınız. Sonra voltajı anma değerinde sabit tutunuz. Kalkış sırasında ve motor dengeye gelene kadar bir süre yük torku sıfır olsun. Sonra aniden tam yük torkunu uygulayınız. Yine dengeye geldikten bir süre sonra aniden yük torkunu kaldırınız. Dengeye geldikten sonra giriş voltajını rampa inişi şeklinde zıt yönde anma değerine getiriniz ve bu değerde sabit tutunuz. Dengeye gelince öncekiyle aynı işaretli yük torkunu aniden uygulayınız. Bu durumda DC makine jeneratör moduna geçecek ve biraz hızlanacaktır. Dengede bir süre çalıştıktan sonra yük torkunu aniden kaldırınız(sıfırlayınız). Yine dengede bir süre çalıştıktan sonra giriş voltajını aniden sıfırlayınız. Dengeye gelinceye kadar (hız ve akım sıfırlanana kadar) bir süre daha çalıştırıp simülasyonu sonuçlandırınız. Tüm bunlar aynı simülasyon çalışması içinde yer alacaktır. Hız ve yük torkunu uygun bir ölçekle (değişimler farkedilebilecek şekilde) aynı grafik penceresinde, voltaj ve akım grafiklerini de başka bir grafik penceresinde yine uygun ölçeklerle çizdiriniz. Grafikler üzerine Matlab komutlarıyla açıklamalar yazdırınız.</p>
