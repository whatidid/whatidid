						<h4>Deney yerine simülasyon dosyaları<span class="badge">18.3.2021</span></h4>
						<p>Simülasyon dosyaları DC makine deneylerinin tamamı ve föylerde olmayanları için hazırlanmıştır. Bunlar MATLAB ya da Octave ile çalıştırılabilir. Octave ile çalışma oldukça yavaştır (MATLAB'ın 10-12 katı sürede) fakat yine de her birinin 2-3 dakikayı geçeceğini sanmıyorum.<br>
						<a href="https://atasevinc.net/emlab/DC_makine_deney_simulasyonlari.zip">Tüm DC makine deney simülasyon dosyaları</a><br>
						Simülasyonlarda hem histerezis etkisi hem de ksmen armatür reaksiyonu dikkate alınmıştır. Seri, şönt, kompund (ekelemeli ve çıkarmalı), yabancı uyartımlı, sabit mıknatıslı motor ve jeneratör modları dahildir.<br><br>
						<code>BHisterezis.m</code> dosyası, histerezis etkisini dikkate almak için gereken bir dosyadır. Tek başına çalıştırmanız gerekmemektedir.<br>
						<code>DCjenerator.m</code> dosyası gerekli ayarları yapılarak istenen tüm DC jeneratör deneyi için kullanılabilir.<br>
						<code>DCmotor.m</code> dosyası gerekli ayarları yapılarak istenen tüm DC motor deneyi için kullanılabilir.<br>
						Ancak ayarları yapamayabileceğinizi düşünerek her bir bağlantı veya çalıştırma modu için doğrudan çalıştırabileceğiniz ayarları yapıp ayrı ayrı dosyalar halinde depaylaştım. Dosya isimlerinden anlaşılıyor.<br>
						Bunların her birini çalıştırıp grafikleri inceleyiniz. Grafik penceresindeki ve eksenlerindeki açıklamaları inceleyiniz.</p>
						<br><br>
						<p>Notlar büyük ölçüde rapor notlarına göre olacaktır. Ayrıca vize, final/bütünleme sınavları yapılmayacaktır. Şöyle ki:
<br>Vize notu, TR-1, TR-2, DC-1 ve DC-2 rapor notları (10 üzerinden) toplamının 2,5 ile çarpımı
<br>Final notu, raporsuz olan AC-0 ve AC-3 deneylerinden katıldıkları için 5'er puan ile DC-3, DC-4, DC-5, AC-1 ve AC-2 rapor notları toplamının 10/6 ile çarpımı olacaktır.
<br>Bütünleme notu, yukarıdaki gibi hesaplanan notlarının düşük olmasına neden olan raporlarının doğru düzgün yeniden yazılıp getirilmesi halinde geçmesi için gereken notun verilmesi ya da yetersiz kalan notunun verilmesi şeklinde olacaktır.</p>
						<p>Notlar muhtemelen 60 üzeri sınıf ortalamasına göre olacaktır.</p>
