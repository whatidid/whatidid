						<h3>Bitirme Projeleri</h3>
						<p>2021-2022 eğitim-öğretim yılında Bitirme Projesi 1-2 dersleri için vermeyi düşündüğüm proje konularının bir kısmı aşağıda verilmiştir. Projeler aksi belirtilmedikçe 2 dönemliktir. Ancak 2 dönemlik projeler için <span class="text-primary"><strong>ilk dönem uygulamalı çalışma</strong></span>, ikinci dönem ise uygulamadaki eksikliklerin giderilmesi ve rapor hazırlanması şeklinde istiyorum. Öğrencinin ilk dönem başarıyla çalıştıramadığı bir uygulamayla geçebilmesi için, <em>devrenin makul bir tasarımla finaller başlamadan 1 hafta öncesine kadar tamamlanmış olarak gösterilmesini <strong>ve</strong> finaller bitinceye kadar, çalışmama nedeniyle ilgili tahminler, giderilmesi için yapılanlar ve sonuçlarıyla ilgili <strong>çok sayıda</strong> denemeyi anlatan gayri resmi nitelikte (ödev yazım kuralları aranmaksızın, el yazısı da olabilir) bir rapor verilmesini</em> isterim.</p>
