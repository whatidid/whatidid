						<h3>Yarı İletkenler</h3>
						<p>Aşağıda NPN ve PNP transistörlerin çalışma ilkeleri yeniden anlatılmıştır. Koyu yazılan kısım transistörün nasıl akım yükseltmesi yaptığının püf noktasıdır.</p><br>
						<p>Aşağıda herhangi bir hata bulursanız lütfen <span class="text-primary">A.Sevinc@kku.edu.tr</span> adresine bildiriniz.</p>
						<h4><strong>NPN TRANSİSTÖRÜN ÇALIŞMA İLKESİ</strong></h4>
						<img src="https://atasevinc.net/yi/NPN.jpg"><br>
						<p>Soldaki NP eklemi ters kutuplanırsa taşıyıcılardan boşaltılmış bölge genişler. Eklem yüzeyinin P tarafında (-) yüklü alıcı iyonlar, N tarafında (+) yüklü verici iyonlar bulunduğundan, şekildeki yönde bir ξı elektrik alanı vardır. Bu alan çoğunluk taşıyıcılarının geçişine izin vermez. Azınlık taşıyıcısı olmadığı sürece bu eklemden akım geçmez.</p>
						<p>Sağdaki PN eklemi doğru kutuplanırsa bu eklemin boşaltılmış bölgesi daralır. VBE voltajı, ξiç elektrik alanına zıt yönde, onu yenecek bir alan oluşturacak büyüklükteyse (iletim voltajına ulaşmışsa) çoğunluk taşıyıcıları bu eklemi geçerler ve B’den E’ye bir akım geçer. <strong>N bölgesinin çoğunluk taşıyıcıları olan serbest elektronlar P bölgesine ulaştıklarında, bu bölge çok ince ve yüksek özdirençli olduğu için, çoğu B ucuna ulaşamadan kendilerini soldaki eklemin taşıyıcılardan boşaltılmış bölgesinde bulurlar. P bölgesi için azınlık taşıyıcısı sayılan bu elektronlar, ξı elektrik alanının yardımıyla kolayca soldaki N bölgesine geçerler. Buradan da VCB geriliminin (+) kutbu tarafından çekilirler ve şekildeki yönde bir iC akımı oluşur.</strong></p>
						<p>P bölgesindeki serbest elektronların ancak az bir kısmı B ucuna ulaştığı için iC >> iB olur. Yani kollektör akımı (iC), beyz akımının (iB)  yükseltilmişi olarak elde edilir.</p>
						<br>
						<h4><strong>PNP TRANSİSTÖRÜN ÇALIŞMA İLKESİ</strong></h4>
						<img src="https://atasevinc.net/yi/PNP.jpg"><br>
						<p>Soldaki PN eklemi ters kutuplanırsa taşıyıcılardan boşaltılmış bölge genişler. Eklem yüzeyinin N tarafında (+) yüklü verici iyonlar, P tarafında (-) yüklü alıcı iyonlar bulunduğundan, şekildeki yönde bir ξı elektrik alanı vardır. Bu alan çoğunluk taşıyıcılarının geçişine izin vermez. Azınlık taşıyıcısı olmadığı sürece bu eklemden akım geçmez.</p>
						<p>Sağdaki NP eklemi doğru kutuplanırsa bu eklemin boşaltılmış bölgesi daralır. VEB voltajı, ξiç elektrik alanına zıt yönde, onu yenecek bir alan oluşturacak büyüklükteyse (iletim voltajına ulaşmışsa) çoğunluk taşıyıcıları bu eklemi geçerler ve E’den B’ye bir akım geçer. <strong>P bölgesinin çoğunluk taşıyıcıları olan holler N bölgesine ulaştıklarında, bu bölge çok ince ve yüksek özdirençli olduğu için, çoğu B ucuna ulaşamadan kendilerini soldaki eklemin taşıyıcılardan boşaltılmış bölgesinde bulurlar. N bölgesi için azınlık taşıyıcısı sayılan bu holler, ξı elektrik alanının yardımıyla kolayca soldaki P bölgesine geçerler. Buradan da VBC geriliminin (-) kutbu tarafından çekilirler ve şekildeki yönde bir iC akımı oluşur.</strong></p>
						<p>N bölgesindeki hollerin ancak az bir kısmı B ucuna ulaştığı için  iC >> iB  olur. Yani kollektör akımı (iC), beyz akımının (iB) yükseltilmişi olarak elde edilir.</p>
						<h4><strong>BJT TERS KULLANILABİLİR Mİ?</strong></h4>
						<p>BJT'nin emiter ve kollektörünün birbiri yerine kullanılması halinde akım kazancı β çok küçük olur. Küçük akımlarda 1-2 civarında olur. Bu da tercih edilmeyen bir kullanım olur.</p>
						<h4><strong>BİR BJT, İKİ YÖNLÜ ANAHTAR OLARAK KULLANILABİLİR Mİ? </strong></h4>
						<p>Soruyu biraz açalım: BJT'nin ters kullanımının sakıncası sadece kazancın düşmesi ise, onu anahtar olarak kullanırken kazancını umursamasak, tek bir BJT'yi çift yönlü anahtar olarak kullanmak ekonomik bir çözüm olur mu? Zira IGBT, MOSFET gibi anahtar elemanlar doyumdayken üzerinde 3,5-4V gerilim düşümü olurken BJT'de bu 0,2-0,3V'a kadar düşebilmektedir. Böylece çok daha az kayıpla çalışmış olmaz mıyız?</p>
						<p>Cevap: Tek bir BJT bu amaçla kullanılabilir görünse de güç elektroniğinde böyle bir kullanım hiç tercih edilmez. Nedenleri:</p>
						<ul>
							<li>1) Ters kullanımda β çok küçüktür ve doyma bölgesi civarında çok daha küçüktür, 1'den bile oldukça küçüktür. Yani ters kullanımda elemanı doyuma getirmek için kollektörden geçireceğimiz akımdan daha fazlasını beyzden geçirmek zorunda kalırız. Yani kontrol kaybı yine büyük olur.</li>
							<li>2) Ayrıca akım yükselince beyz ile emiter (aslında kollektör olup ters kullandığımız için emiter dediğimiz uç) arasındaki gerilim 0,7V'ta kalmaz, çok daha artar. Bu da kontrol kaybını daha da artırır.</li>
							<li>3) Tüm bu kayıplara razı olsak bile elemanı muhtemelen elle değil, elektronik bir devreyle  otomatik olarak anahtarlamak isteriz. Bu durumda beyz ile emiter (aslında kollektör olup ters kullandığımız için emiter dediğimiz uç) arasına yüksek akım taşıyabilen başka bir yarı iletken anahtar kullanmamız gerekir ve optocoupler'ler pek yüksek akım taşıyamaz. Yani BJT'yi ters anahtar olarak kullanmak için yaklaşık aynı akımlı başka bir yarı iletken anahtar kullanmamız gerekmesinden dolayı böyle bir kullanım saçma olur.</li>
						</ul>
