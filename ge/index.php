    <!-- Menu Toggle Script -->
    <script>
	if ($(window).width() < 768) {	
		$("#menu-toggle").hide();
		$("#menu-toggle2").show();
	}
	else {
		$("#menu-toggle2").hide();
		$("#menu-toggle").show();
	}
	

	$(window).on('resize', function() {
    
        if ($(window).width() < 768) {
            $("#menu-toggle").hide();
			$("#menu-toggle2").show();
        }
		else {
			$("#menu-toggle2").hide();
			$("#menu-toggle").show()
        }
	});
    $("#menu-toggle").click(function(e) {
        e.preventDefault();
        $("#wrapper").toggleClass("toggled");
		$("#menu-toggle").hide();
		$("#menu-toggle2").show();
    });
	$("#menu-toggle2").click(function(e) {
        e.preventDefault();
        $("#wrapper").toggleClass("toggled");
		$("#menu-toggle2").hide();
		$("#menu-toggle").show();
    });
	var toggle_flag = 0;
	$('.nav-header').click(function () {
		//$(this).find('i').toggleClass('glyphicon-menu-down').toggleClass('glyphicon-menu-up');
		if (toggle_flag == 1) {
			$(this).find('i').addClass('glyphicon-menu-down').removeClass('glyphicon-menu-up');
			$(this).nextUntil('.nav-header').hide(200);
			toggle_flag=0;
		}
		else {
			$(this).find('i').addClass('glyphicon-menu-up').removeClass('glyphicon-menu-down');
			$(this).nextUntil('.nav-second-level').show(200);
			$(this).nextUntil('.nav-header').filter('.nav-second-level').show(200);
			toggle_flag=1;
		}
	});
	$('.nav-second-level').click(function() {
		$(this).nextUntil('.nav-second-level').toggle(200);
	});
	
	
    </script>

