	if ($(window).width() < 768) {
		$("#menu-toggle").hide();
		$("#menu-toggle2").show();
	}
	else {
		$("#menu-toggle2").hide();
		$("#menu-toggle").show();
	}

	$(window).on('resize', function() {
    
        if ($(window).width() < 768) {
            $("#menu-toggle").hide();
			$("#menu-toggle2").show();
        }
		else {
			$("#menu-toggle2").hide();
			$("#menu-toggle").show();
        }
	});
    $("#menu-toggle").click(function(e) {
        e.preventDefault();
        $("#wrapper").toggleClass("toggled");
		$("#menu-toggle").hide();
		$("#menu-toggle2").show();
    });
	$("#menu-toggle2").click(function(e) {
        e.preventDefault();
        $("#wrapper").toggleClass("toggled");
		$("#menu-toggle2").hide();
		$("#menu-toggle").show();
    });
