<h3>3.2. Araştırma-Geliştirme Aşamaları</h3>
<div><a href="javascript:hideshow(document.getElementById('literatur'))">Literatür taraması</a></div>
	<div id="literatur" style="display: none;" class="tab" align="justify">
	Araştırmaya genellikle literatür taramasıyla başlamak gerekir. Mevcut uygulamalar, geliştirmeler hem yazılı hem görsel kaynaklarda taranır. Başlıca çalışmalar ve onlarda gösterilen kaynaklar üzerinden daha faydalı literatür özeti çıkarılabilir. En çok atıf alan veya izlenen çalışmaların o alandaki başlıca çalışmalar olduğunu düşünsek de şart değildir. Bazı değerli çalışmalar yıllar sonra fark edilmekte, birçoğu ise unutulup gitmektedir. Bu yüzden hızlı ve isabetli değerlendirme için çalışmanın "Özet" ve "Sonuç" kısımlarını okuyup kendimiz karar vermemiz en doğrusudur. En azından işine yarayıp yaramayacağını yorumlayacak kadar anlayarak okumak gerekir. ("Okumak" derken duruma göre dinlemek, izlemek, gözlemek de aynı kapsamdadır.) İşimize yarayacağını düşündüğümüz yayınları dikkatlice okumalıyız. Okurken hangi bilgileri aradığımızı unutmamalı, gereksiz ayrıntılarda boğulmamalı, gereken ayrıntıları ise atlamamalıyız.<br>
	<br>
	Literatür özeti, "falan şunu yapmış, filan bunu yapmış" düzeniyle kopuk kopuk sunulmamalıdır. Gelişim sırası ve mantıksal ilişkilere göre "şunu falan şöyle yapmış, bunu filan böyle yapmış" düzeniyle sunulmalıdır. Yani kimin ne yaptığı değil, neyi kimin nasıl yaptığı önemlidir.
	</div><br>
<div><a href="javascript:hideshow(document.getElementById('teorik_temel'))">Teorik temelin güçlendirilmesi</a></div>
	<div id="teorik_temel" style="display: none;" class="tab">
	Literatür taraması sonucu önemli olduğu anlaşılan çalışmalara iyice çalışılmalı, matematiksel anlatımlar, devre ve programların çalışması vb iyice kavranarak öğrenilmelidir. Mevcut başlıca birkaç yöntemin karşılaştırması yapılarak birbirine göre avantaj ve dezavantajları kavranmalıdır.<br>
	<br>
	Bunlar yapılmadan geliştirme yapma ihtimali çok düşük olduğu gibi, yapılsa bile ileride fark edilecek zorluklar daha çok zaman kaybettirebilir. Mevcut çalışmaları deneysel olarak tekrarlamak genellikle fazla zaman ve masraf gerektirdiğinden tercih edilmez. Fakat mümkünse simülasyon ortamında gerçekleştirilmesi oldukça faydalı olur.<br>
	<br>
	"Simülasyon" daha sonraki bir konumuzdur.
	</div><br>
</div>
<div><a href="javascript:hideshow(document.getElementById('teorik_gel'))">Teorik geliştirme</a></div>
	<div id="teorik_gel" style="display: none;" class="tab">
	<i>Bu adım, nadiren ve pratik yönü oldukça ağır basan çalışmalarda atlanabilir veya pratik geliştirme sonrasına bırakılabilir (önce pratik geliştirip açıklamasını sonra aramak).</i><br>
	<br>
	Tasarım veya yöntemler önce kâğıt üstünde planlanır. Sonra bilgisayar simülasyonlarıyla doğrulanması tavsiye edilir. Çünkü genellikle simülasyon ortamında yanlışlanma, deneysel olarak da yanlışlanacağını ispatlar. Aksi ise pek doğru sayılmaz; yani simülasyon ortamında doğrulanma, deneysel olarak doğrulanacağını garanti etmez. Bu yüzden simülasyonla yanlışlanan bir fikri deneysel ortamda da test etmek genellikle zaman kaybı olur.
	</div><br>
	<div><a href="javascript:hideshow(document.getElementById('deneysel'))">Deneysel araştırma-geliştirme</a></div>
	<div id="deneysel" style="display: none;" class="tab">
	<i>Bu adım, çalışmanın konu icabı teori veya yazılımdan ibaret olduğu bazı alanlarda yer almaz.</i><br>
	<br>
	Simülasyon ortamında veya kâğıt üzerinde tasarımlarda başarılı görünen fikirler deneysel ortamda da doğrulanmaya genellikle muhtaçtır. Çünkü bazı pratik engellerin gözardı edilmiş olma ihtimali vardır. Meselâ simülasyon nedenselliğe aykırı, veya ölçülemeyecek bilgiler kullanılarak yapılmış olabilir. Gürültü gibi belirsizlik taşıyan bilgiler çok hassas biliniyor varsayılmış olabilir. Başka bazı mantık hataları da olabilir.<br>
	<br>
	Deneysel çalışmanın ilk denemelerde başarısız olması, denenen fikrin yanlış olduğu anlamına gelmeyebilir. Pek çok deneme hatası yapılmış olabilir. <a href="https://atasevinc.net/projeders/aty1_konu04_hata_bulmak.htm">Hata bulma yöntemleri</a> kullanılarak fikrin çalıştırılması için ısrar edilir. İyi bir teori ile pratiğin uymaması prensip olarak mümkün değildir; yani tüm hata ayıklamalarına rağmen uyuşmazlık görünüyorsa işte bu durum yeni tecrübe kazanacağınız durum olup, teorinizi iyileştirmeniz gerektiği içindir. Hesaba katmadığınız bir şeyleri daha (gürültü analizi gibi) teorinize katmalısınız.
	<br><br>
	Deneyde kullanılan techizatın kalitesi çok önemli olabilmektedir. Kalitesiz techizat, belirsizlikleri çok artırabilir, yanıltabilir, çalışmaları aksatabilir.
	<br><br>
	Deneydeki başarısızlığın, fikrin kötü olmasından kaynaklandığı anlaşılsa bile hemen pes edilmemeli, takviye edici ya da en baştan yeni fikirler üreterek başarılı sonuçlar çıkarmaya çalışılmalıdır. Deney düzeneği kurmuş ve tecrübe kazanmış olmanın bir şekilde meyvesini almak istenir.
</div>
