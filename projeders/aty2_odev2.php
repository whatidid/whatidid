<h2>Düşünme Hataları</h2>
<p>Bu ödevin konusudur. Diğer ödevlerin aksine, bu ödevle ilgili konu anlatımı paylaşmayacağım. Bunun bir sebebi, bu konu üzerinde ne yazarsam yazayım çok eksik kalacak olması; çünkü çok çeşitli düşünme hataları var. Ama daha önemlisi, bu konu üzerinde bir ders vermenin, bir bakıma insanları nasıl aldatacağınızı öğretmek anlamına da gelmesi. Ama ödev olunca pek öyle olmuyor, kendiniz yaşadıklarınızdan, rahatsız olduklarınızı veya paylaşmak istediğiniz türde düşüncelerinizi örneklerle anlatmış olacaksınız.</p>
<p>Gelişmemekte ısrar eden toplumların başlıca sorunlarından birini ele alıyoruz. Bilimsel, ticari, siyasi, psikolojik ve gündelik pek çok olayda çok yapılan düşünme hatalarını sınıflandıracak ve örnekler vereceksiniz.</p>
<p>Hepimiz karşımızdaki insanlarda gördüğümüz kimi kasıtlı ve aldatmaya yönelik, kimi boş bulunmaktan, kimi saflıktan, kimi umursuzluktan pek çok mantık hatalarına çok defa sinirlenmiş, bazen de gülmüşüzdür. Tabii bunları kendimizde de bulup düzeltmeye çalışmamız gerekir. Okumuş yazmış insanlar olarak mantık hatalarıyla aldanmamız veya aldatmamız yakışık almaz. Bilimsel çalışmalarımızda da neyi neyle karşılaştırmak gerektiğini bilmek, hangi sonucun nasıl yorumlanıp yorumlanamayacağına karar verebilmek için de düşünmeyi bilmek, düşünme ve mantık hatalarından kaçınmak gerekmektedir. Toplum olarak gelişmemiz için bu hatalardan kurtulmamız çok önemlidir. Eğitim hayatımızdaki en önemli konulardan biri budur.</p>
<p>Düşünme hatalarını tanımlayıp sınıflandırmak, bunlara karşı uyanık olmayı ve gerektiğinde kolayca ifade etmeyi sağlar. Aksi halde, apaçık bir mantık hatası karşısında ifade zorluğu yaşamak çok sinir bozucu olabilir. Örnekleri ve sınıflandırmayı sizden beklediğim ve sizin vereceğiniz cevapları kısıtlamamak için normalde olmayacak, karikatürize edilmiş örnekler vereyim:</p>
<p><code>Gerçekler acıdır. Biber de acıdır. Öyleyse biber de bir gerçektir.</code></p>
