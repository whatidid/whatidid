<p>Şimdi gelin de buradaki mantık hatasını ciddi ciddi açıklamaya kalkın, tıkanırsınız; zaten bu yüzden gülersiniz. Pek çok fıkra, deyim, atasözü, replik, mantık hatalarını ciddi ciddi açıklamak yerine karikatürize ederek gözler önüne sermek için üretilmiştir. Ancak bazen bunlarda da mantık hatalarına rastlamaktayız. Mesela Temel'in lokantada zeytine çatal saplayamama fıkrasına yapılan göndermelerin, genellikle fıkrada dalga geçilen görüşü destekleme amaçlı olduğu hep gözümüzden kaçar.</p>
<p>Düşünme hatalarına her zaman gülüp geçmek, fıkralara, deyimlere gönderme yapmak olmuyor. Kendimizin veya karşımızdakinin konumu gereği, diplomatik bir dille ifade etmemiz gerekebiliyor. Ayrıca esprili göndermeler, kendimiz de mantık hatası yapıyorsak bilakis kendimizi gülünç duruma düşürebilir. Karşımızdakinden daha güçlü konumda olmak bizi küçük düşmekten kurtarmaz. Ayrıca insanlarla dalga geçmek hoş da değildir. Düşünme hatalarını güzelce açıklamak ve sınıflandırmak bu yüzden de önemlidir.</p>
