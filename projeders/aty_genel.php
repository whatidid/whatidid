<h1>Araştırma ve Tasarım Yöntemleri 1-2 derslerini işleyişim hakkında genel açıklamalar</h1>
(Son düzenleme: <font color="red">9.10.2021</font>)
<font size=4>
<p>Diğer hocalar farklı işleyebilir ama benim bu dersi işleyiş şeklim şöyle olacak:</p>

<p>Zaman zaman bu sitede bazı anlatımlar yayınlayacağım. Anlaşılmayan yerleri e-posta ile sorarsanız yine e-posta ile cevaplamaya çalışacağım. Bu ders ile ilgili tüm mesajlaşmalarımızda konu satırı ATY ile başlasın. Bu standarda uyarsak daha kolay ve kaybolmayan kayıtlarla iletişim kurarız. Müsait zamanlarımda yakalamanız halinde yüz yüze de sorabilirsiniz. Ancak karıştırılmaması gereken bir şey var. Ben bu anlatımlarda size her zaman hazır bilgi değil, bilgiye erişmek, yorumlamak, süzmek ve kullanmak hususlarında yönlendirme vereceğim. Her birinizin erişmesi gereken bilgiler farklı olabilir.</p>

<p>Bu anlatımlar doğrultusunda bazı ödevler yapmanızı isteyeceğim. Her ödev 2-3 haftalık küçük iş paketleri içerecek. Ödevlerden alacağınız notlara göre vize ve final/büt notlarınızı belirleyeceğim. Toplam 3-5 adet ödev verip bunların belirteceğim ağırlıklardaki ortalamasını bu dersin başarı notu olarak (vize-final ayrı değil, tek not) gireceğim.</p>

<p>Ödevlerin birbirinizden ve hatta herhangi bir kaynaktan aynen alıntılanmaması, özümsenip kendi cümlelerinizle yazılması çok önemli. Çok benzer anlatımlara ya puan vermeyeceğim, ya da benzer getirenlere bir kişilik puanı paylaştıracağım. Bu yüzden aynısını veya çok benzerini yazmak üzere arkadaşlarınıza ödevlerini sizinle paylaşmalarını teklif bile etmeyiniz.</p>

<p>Ödevlerin zamanında yapılması önemli. Yani durup durup birkaç haftalık ödevi birden getirmenizi kabul etmeyeceğim. Zaten bu dersin amacı doğru ve düzenli araştırma yapmayı öğrenmek. Düzensizlikle bunu baştan bozmamalıyız.</p>

<p>Yeni anlatım veya ödev yayınlayınca bunu, öğrenci otomasyon sisteminde kayıtlı e-posta adresinize e-posta ile duyururum. Bu yüzden o e-posta adresinizin güncel kullandığınız bir adres olması önemli.</p>

<p>Hepinize başarılar diliyorum.</p></font>
